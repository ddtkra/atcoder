#!/usr/bin/env python3
import sys
sys.setrecursionlimit(10000000)
INF = 1<<32
w =  ['SUN','MON','TUE','WED','THU','FRI','SAT']

def solve(S: str):
    we = w+w
    n = w.index(S)
    if n == 0:
        print(7)
    else:
        print(we[n:].index('SUN'))
    return



def main():
    def iterate_tokens():
        for line in sys.stdin:
            for word in line.split():
                yield word
    tokens = iterate_tokens()
    S = next(tokens)  # type: str
    solve(S)

if __name__ == '__main__':
    main()
