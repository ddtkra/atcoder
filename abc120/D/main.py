def solve(N: int, M: int, A: "List[int]", B: "List[int]"):
    ans = []

    uf = UnionFind(N)
    ans.append((N-1)*N//2)
    for i in range(M-1, 0, -1):
        if uf.find(A[i]) != uf.find(B[i]):
            ans.append(max(0, ans[-1]-uf.size(A[i])*uf.size(B[i])))
            uf.union(A[i], B[i])
        else:
            ans.append(ans[-1])
    
    for i in reversed(range(len(ans))):
        print(ans[i])

