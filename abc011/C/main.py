def main():
    def iterate_tokens():
        for line in sys.stdin:
            for word in line.split():
                yield word
    tokens = iterate_tokens()
    N = int(next(tokens))  # type: int
    NG = [ int(next(tokens)) for _ in range(3) ]  # type: "List[int]"
    solve(N, NG)

if __name__ == '__main__':
    main()
